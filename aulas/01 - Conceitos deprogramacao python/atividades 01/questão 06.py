"""
Faça um algoritmo que solicite ao usuário informar 3 números. Em seguida, some-os e
envie para a saída padrão a seguinte frase: "A soma total é: "
"""
num1 = int(input("Informe o primeiro número: "))
num2 = int(input("Informe o segundo número: "))
num3 = int(input("Informe o terceiro número: "))
soma =  num1+num2+num3;
print("A soma total é: %d" %soma)