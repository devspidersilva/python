"""
Faça um algoritmo que solicite a largura e a altura de um retângulo. Em seguida,
imprima para o usuário quantos metros quadrados possui a área total.
"""
num1 = float(input("Informe a altura: "))
num2 = float(input("Informe a largura: "))

area = num1*num2;
print("Metros quadrados: %.2f²" %area)