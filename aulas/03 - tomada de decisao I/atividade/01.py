"""""
Faça um algoritmo que leia um número e mostre se o mesmo é positivo, negativo ou zero.

"""

num1 = int(input("digite um número: "))

if(num1>0):
    print("Positivo")
elif(num1<0):
    print("Negativo")
elif("num1 == 0"):
    print("Esse numero é igual a zero")
else:
    print("número invalido!!!")